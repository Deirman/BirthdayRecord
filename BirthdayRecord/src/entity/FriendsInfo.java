package entity;

import java.util.Date;

public class FriendsInfo {
	String friend_name;
	String friend_sex;
	String friend_birthday;
	String friend_appellation;
	String friend_phone;
	String friend_address;
	
	
	public String getFriend_name() {
		return friend_name;
	}
	public void setFriend_name(String friend_name) {
		this.friend_name = friend_name;
	}
	public String getFriend_sex() {
		return friend_sex;
	}
	public void setFriend_sex(String friend_sex) {
		this.friend_sex = friend_sex;
	}
	public String getFriend_birthday() {
		return friend_birthday;
	}
	public void setFriend_birthday(String friend_birthday) {
		this.friend_birthday = friend_birthday;
	}
	public String getFriend_appellation() {
		return friend_appellation;
	}
	public void setFriend_appellation(String friend_appellation) {
		this.friend_appellation = friend_appellation;
	}
	public String getFriend_phone() {
		return friend_phone;
	}
	public void setFriend_phone(String friend_phone) {
		this.friend_phone = friend_phone;
	}
	public String getFriend_address() {
		return friend_address;
	}
	public void setFriend_address(String friend_address) {
		this.friend_address = friend_address;
	}
	
	
}
